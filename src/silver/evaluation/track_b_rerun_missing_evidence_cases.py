# -*- coding: utf-8 -*-
"""Rerun Track B cases with incomplete diagnostic evidence.

The target set is generated by the evidence audit. This script archives previous
case directories, removes affected rows from aggregate files, reruns the exact
same samples, and writes refreshed aggregate artifacts. It does not fabricate
or backfill missing execution_result.json files.
"""

from __future__ import annotations

import argparse
import csv
import json
import shutil
import time
from collections import Counter
from pathlib import Path
from typing import Any

from silver.evaluation.track_b_full_official_eligibility import (
    FULL_MANIFEST,
    PHASE,
    PHASE_ROOT,
    row_result,
    run_attempt_with_retry,
    summarize,
    summarize_by_task,
    write_progress_doc,
)
from silver.evaluation.vlabench_generic_official_phase import (
    read_jsonl,
    safe_rel,
    write_csv,
    write_json,
    write_jsonl,
)


TARGET_PATH = PHASE_ROOT / "evidence_audit" / "missing_evidence_cases.jsonl"


def now_iso() -> str:
    return time.strftime("%Y-%m-%d %H:%M:%S")


def log(message: str) -> None:
    print(f"[{now_iso()}] {message}", flush=True)


def read_csv_rows(path: Path) -> list[dict[str, Any]]:
    if not path.exists():
        return []
    with path.open("r", encoding="utf-8-sig", newline="") as f:
        return list(csv.DictReader(f))


def read_targets(limit: int | None = None) -> list[dict[str, Any]]:
    rows = read_jsonl(TARGET_PATH)
    if limit is not None:
        rows = rows[:limit]
    return rows


def archive_case_dir(case_dir: Path, archive_root: Path) -> str | None:
    if not case_dir.exists():
        return None
    archive_target = archive_root / case_dir.relative_to(PHASE_ROOT / "data" / PHASE)
    archive_target.parent.mkdir(parents=True, exist_ok=True)
    if archive_target.exists():
        archive_target = archive_target.with_name(f"{archive_target.name}_{time.strftime('%H%M%S')}")
    shutil.move(str(case_dir), str(archive_target))
    return str(archive_target)


def sample_index(row: dict[str, Any]) -> int:
    return int(row["sample_index"])


def write_report(report: dict[str, Any]) -> None:
    doc = PHASE_ROOT / "evidence_audit" / "TRACK_B_MISSING_EVIDENCE_RERUN_REPORT.md"
    lines = [
        "# Track B Missing Evidence Rerun Report",
        "",
        "## 목적",
        "",
        "Track B failure taxonomy의 재현성을 보강하기 위해 evidence audit에서 `execution_result.json` 누락이 확인된 diagnostic sample을 재실행했다. 기존 artifact는 삭제하지 않고 archive로 이동했으며, 재실행 결과만 aggregate에 반영했다.",
        "",
        "## 원칙",
        "",
        "- mock/synthetic result 생성 금지",
        "- 기존 dataset 파일 수정 금지",
        "- 기존 case artifact는 archive 보존",
        "- 재실행 후 aggregate CSV/JSONL/summary를 저장 artifact에서 재계산",
        "",
        "## 요약",
        "",
        f"- 시작 시각: {report['started_at']}",
        f"- 완료 시각: {report['completed_at']}",
        f"- 대상 sample: {report['target_count']}",
        f"- 재실행 완료: {report['rerun_count']}",
        "",
        "## 재실행 후 상태 분포",
        "",
    ]
    for key, value in report["rerun_status_counts"].items():
        lines.append(f"- {key}: {value}")
    lines.extend(
        [
            "",
            "## 대상별 결과",
            "",
            "| sample_index | sample_id | old_status | new_status | repeat_1 | repeat_2 | archive |",
            "|---:|---|---|---|---|---|---|",
        ]
    )
    for row in report["rerun_rows"]:
        lines.append(
            "| {sample_index} | `{sample_id}` | {old_status} | {official_status} | {repeat_1_status}/{repeat_1_success} | {repeat_2_status}/{repeat_2_success} | `{archive_path}` |".format(
                **row
            )
        )
    lines.extend(
        [
            "",
            "## 재집계 상태",
            "",
            f"- processed_rows: {report['summary']['processed_rows']} / {report['summary']['total_manifest_rows']}",
            f"- official_eligible: {report['summary']['official_eligible']}",
            f"- completed: {report['summary']['completed']}",
            "",
            "## Evidence",
            "",
            f"- target list: `{TARGET_PATH}`",
            f"- archive root: `{report['archive_root']}`",
            f"- results CSV: `{report['result_path']}`",
            f"- summary JSON: `{report['summary_path']}`",
        ]
    )
    doc.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> int:
    parser = argparse.ArgumentParser(description="Rerun Track B missing-evidence cases.")
    parser.add_argument("--timeout-s", type=int, default=300)
    parser.add_argument("--max-retries", type=int, default=2)
    parser.add_argument("--limit", type=int, default=None)
    args = parser.parse_args()

    if not TARGET_PATH.exists():
        raise SystemExit(f"missing target list: {TARGET_PATH}")

    started_at = now_iso()
    result_path = PHASE_ROOT / "metrics" / f"{PHASE}_official_results.csv"
    ledger_path = PHASE_ROOT / "manifest" / f"{PHASE}_official_eligibility_ledger.jsonl"
    retry_path = PHASE_ROOT / "metrics" / f"{PHASE}_retry_log.jsonl"
    summary_path = PHASE_ROOT / "metrics" / f"{PHASE}_official_summary.json"
    archive_root = PHASE_ROOT / "archived_invalid_runs" / ("missing_evidence_rerun_" + time.strftime("%Y%m%d_%H%M%S"))

    targets = read_targets(args.limit)
    target_ids = [row["sample_id"] for row in targets]
    target_set = set(target_ids)
    manifest = read_jsonl(FULL_MANIFEST)
    manifest_by_id = {row["sample_id"]: (index, row) for index, row in enumerate(manifest, start=1)}
    missing_targets = [sid for sid in target_ids if sid not in manifest_by_id]
    if missing_targets:
        raise SystemExit(f"Target samples not found in manifest: {missing_targets[:10]}")

    old_rows_by_id = {row["sample_id"]: row for row in read_csv_rows(result_path)}
    existing = {sid: row for sid, row in old_rows_by_id.items() if sid not in target_set}
    retry_rows = [row for row in read_jsonl(retry_path) if row.get("sample_id") not in target_set]

    rerun_rows: list[dict[str, Any]] = []
    for target in targets:
        sid = target["sample_id"]
        index, row = manifest_by_id[sid]
        case_dir = PHASE_ROOT / "data" / PHASE / safe_rel(row)
        archived = archive_case_dir(case_dir, archive_root)
        old_status = old_rows_by_id.get(sid, {}).get("official_status", target.get("official_status", "unknown"))

        log(f"RERUN_MISSING_EVIDENCE sample_index={index} sample_id={sid} old_status={old_status}")
        log(f"  archived_old_case_dir={archived}")

        log(f"  repeat_1 start -> {case_dir / 'official_repeat_1'}")
        rep1, retries1 = run_attempt_with_retry(row, case_dir / "official_repeat_1", args.timeout_s, args.max_retries)
        log(f"  repeat_1 done status={rep1.get('execution_status')} success={rep1.get('condition_success')} retries={len(retries1)}")

        log(f"  repeat_2 start -> {case_dir / 'official_repeat_2'}")
        rep2, retries2 = run_attempt_with_retry(row, case_dir / "official_repeat_2", args.timeout_s, args.max_retries)
        log(f"  repeat_2 done status={rep2.get('execution_status')} success={rep2.get('condition_success')} retries={len(retries2)}")

        retry_rows.extend(retries1)
        retry_rows.extend(retries2)
        new_row = row_result(index, row, rep1, rep2, case_dir)
        existing[sid] = new_row
        rerun_rows.append({**new_row, "old_status": old_status, "archive_path": archived or ""})

        rows = list(existing.values())
        rows.sort(key=sample_index)
        summary = summarize(rows, len(manifest), started_at, completed=len(rows) >= len(manifest))
        write_csv(result_path, rows)
        write_jsonl(ledger_path, rows)
        write_jsonl(retry_path, retry_rows)
        write_json(summary_path, summary)
        write_csv(PHASE_ROOT / "metrics" / f"{PHASE}_summary_by_task.csv", summarize_by_task(rows))
        write_progress_doc(summary, result_path, retry_path)
        log(f"  rerun status={new_row['official_status']} aggregate_eligible={summary['official_eligible']}")

    rows = list(existing.values())
    rows.sort(key=sample_index)
    summary = summarize(rows, len(manifest), started_at, completed=len(rows) >= len(manifest))
    write_csv(result_path, rows)
    write_jsonl(ledger_path, rows)
    write_jsonl(retry_path, retry_rows)
    write_json(summary_path, summary)
    write_csv(PHASE_ROOT / "metrics" / f"{PHASE}_summary_by_task.csv", summarize_by_task(rows))
    write_progress_doc(summary, result_path, retry_path)

    report = {
        "started_at": started_at,
        "completed_at": now_iso(),
        "target_count": len(targets),
        "rerun_count": len(rerun_rows),
        "rerun_status_counts": dict(Counter(row["official_status"] for row in rerun_rows)),
        "rerun_rows": rerun_rows,
        "summary": summary,
        "archive_root": str(archive_root),
        "result_path": str(result_path),
        "summary_path": str(summary_path),
    }
    write_json(PHASE_ROOT / "evidence_audit" / "missing_evidence_rerun_summary.json", report)
    write_report(report)
    print(json.dumps(report, ensure_ascii=False, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
